# ─────────────────────────────────────────────────────────────────────────────

INPUT_FILE  = "index.html"
OUTPUT_FILE = "index_fixed.html"

def normalize(text: str) -> str:
    """Убирает лишние пробелы и переносы строк."""
    return re.sub(r"\s+", " ", text).strip()


def main():
    if not os.path.exists(INPUT_FILE):
        print(f"[ERROR] Файл {INPUT_FILE} не найден. Положи скрипт рядом с index.html")
        return

    with open(INPUT_FILE, "r", encoding="utf-8") as f:
        html = f.read()

    soup = BeautifulSoup(html, "html.parser")

    replaced = 0
    not_found = []

    # Ищем все div.link.title с атрибутом href на /teach/control/...
    for tag in soup.find_all("div", class_="link title"):
        href = tag.get("href", "")
        if not href.startswith("/teach/control/lesson/view/id/"):
            continue

        # Получаем текст урока (без дочернего <span>)
        raw_text = tag.get_text(separator=" ")
        lesson_title = normalize(raw_text)

        # Ищем в маппинге точное совпадение
        local_file = LESSON_MAP.get(lesson_title)

        # Если не нашли точно — пробуем частичное совпадение
        if not local_file:
            for key, val in LESSON_MAP.items():
                if normalize(key) in lesson_title or lesson_title in normalize(key):
                    local_file = val
                    break

        if local_file:
            tag["href"] = local_file
            # Превращаем div в кликабельный элемент (или оборачиваем в <a>)
            # Для удобства также создаём <a> вокруг div
            new_tag = soup.new_tag("a", href=local_file)
            tag.wrap(new_tag)
            replaced += 1
            print(f"  [OK] «{lesson_title}» → {local_file}")
        else:
            not_found.append((href, lesson_title))
            print(f"  [??] Не найдено в маппинге: «{lesson_title}» ({href})")

    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        f.write(str(soup))

    print(f"\n✅ Готово! Заменено: {replaced}. Файл сохранён: {OUTPUT_FILE}")

    if not_found:
        print(f"\n⚠️  Не удалось сопоставить {len(not_found)} уроков:")
        for href, title in not_found:
            print(f"   - «{title}» ({href})")
        print("\nДобавь их вручную в словарь LESSON_MAP в начале скрипта.")


if __name__ == "__main__":
    main()
